package Java_learning_ilp;

public class string_methods {
static String s1=new String("Manisha");
static String s2=new String("learnig java");
public static void main(String [] args) {
    System.out.println("length:"+ s1.length());
    System.out.println("upper:"+s1.toUpperCase());
    System.out.println("lower:"+s1.toLowerCase());
    System.out.println("concat:"+s1.concat(s2));
    System.out.println("charAt:"+s1.charAt(3));
    System.out.println("substring:"+s1.substring(0));
    

}
}
